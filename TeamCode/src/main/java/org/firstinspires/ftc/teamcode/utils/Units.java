package org.firstinspires.ftc.teamcode.utils;

public class Units {
    //////////////
    // DISTANCE //
    //////////////
    public static final double METERS = 1;
    public static final double M = METERS;

    public static final double FEET = 0.3048;
    public static final double FT = FEET;
    public static final double TO_FEET = 1 / FEET;
    public static final double TO_FT = TO_FEET;

    public static final double INCHES = 0.0254;
    public static final double TO_INCHES = 1 / INCHES;

    public static final double CENTIMETERS = 0.01;
    public static final double CM = CENTIMETERS;
    public static final double TO_CENTIMETERS = 1 / CENTIMETERS;
    public static final double TO_CM = CENTIMETERS;

    public static final double MILLIMETERS = 0.001;
    public static final double MM = MILLIMETERS;
    public static final double TO_MILLIMETERS = 1 / MILLIMETERS;
    public static final double TO_MM = TO_MILLIMETERS;

    ///////////
    // ANGLE //
    ///////////
    public static final double RADIANS = 1;

    public static final double DEGREES = 0.0174533;
    public static final double TO_DEGREES = 1 / DEGREES;

    public static final double ROTATIONS = 6.2831853;
    public static final double TO_ROTATIONS = 1 / ROTATIONS;


    //////////
    // TIME //
    //////////
    public static final double SECONDS = 1;
    public static final double S = 1;

    public static final double MINUTES = 60;
    public static final double MIN = MINUTES;
    public static final double TO_MINUTES = 1 / MINUTES;
    public static final double TO_MIN = TO_MINUTES;

    public static final double MILLISECONDS = 0.001;
    public static final double MS = MILLISECONDS;
    public static final double TO_MILLISECONDS = 1 / MILLISECONDS;
    public static final double TO_MS = TO_MILLISECONDS;

    public static final double MILLISECONDS_100 = 0.1;
    public static final double MS_100 = MILLISECONDS_100;
    public static final double TO_MILLISECONDS_100 = 1 / MILLISECONDS_100;
    public static final double TO_MS_100 = TO_MILLISECONDS_100;

}
